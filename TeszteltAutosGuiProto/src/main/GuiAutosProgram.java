    private void btnGarazsbaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGarazsbaActionPerformed
        String autoNeve = txtAutoNeve.getText();
        String autoNevek = "";
        int meret = cmbAutok.getItemCount();
        for(int i = 0; i < meret; i++) {
            autoNevek = cmbAutok.getItemAt(i);
        }
        if("".equals(autoNeve) || autoNeve.equals(autoNevek) ){
                JOptionPane.showMessageDialog(rootPane, "A megadott autó vagy létezik, vagy az 'Autó neve' mező kitöltetlen!");
            
            }else if(!"".equals(autoNeve) && !autoNeve.equals(autoNevek)) {
                cmbAutok.addItem(autoNeve);
            }
    }//GEN-LAST:event_btnGarazsbaActionPerformed

