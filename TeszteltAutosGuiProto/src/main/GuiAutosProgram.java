    private void btnMegyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMegyActionPerformed
        if(cmbAutok.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(rootPane, "Nincs autó kiválasztva!");
        }else{
                Object kivalasztottAuto = cmbAutok.getSelectedItem();
                String kivalasztottAutoStringre = kivalasztottAuto.toString();
                //txaAllapot.append(aktualAuto);
                Object potkerekekSzama = mumPotkerek.getValue();
                String potkerekStringre = potkerekekSzama.toString();
                txaAllapot.setText("Elindítottad az autót.");
                txaAllapot.append("\nJelenlegi Autód: " + kivalasztottAutoStringre);
                txaAllapot.append("\nPótkerekeid száma: " + potkerekStringre);
                if(sldEsely.isEnabled()){
                    int esely = sldEsely.getValue();
                    txaAllapot.append("\nEsélyed a defektre: " + esely + "%");
                }else{
                    txaAllapot.append("\nNincs esély defektre.");
                }
       }
        
        
        
    }//GEN-LAST:event_btnMegyActionPerformed

